import java.util.*;

public class LargestOfTwo{
    public static void main(String args[]){

        int a = 5;
        int b = 8;

        if(a > b){
            System.out.println("a is greater than b");
        }else if(a < b){
            System.out.println("b is greater than a");
        }else{
            System.out.println("Both are equal");
        }

    }
    
}
